package com.lrh.ioc.dependence.inject.domain;

public class UserHolder2 {

    private User user;

    public UserHolder2(User user) {
        this.user = user;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    @Override
    public String toString() {
        return "UserHolder{" +
                "user=" + user +
                '}';
    }
}
